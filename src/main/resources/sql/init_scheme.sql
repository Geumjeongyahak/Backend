-- 봉사 신청: 기존 과목에 교사로 지원하거나 새 과목을 제안
-- application_type: EXISTING_SUBJECT | NEW_SUBJECT
-- 승인 시: 신청자가 TEACHER가 됨, NEW_SUBJECT면 subjects 레코드 생성
CREATE TABLE volunteer_applications (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    applicant_id BIGINT NOT NULL,
    application_type VARCHAR(20) NOT NULL,

    -- EXISTING_SUBJECT 선택 시
    subject_id BIGINT,

    -- NEW_SUBJECT 제안 시 (classroom_id는 관리자가 승인 시 배정)
    proposed_classroom_id BIGINT,
    proposed_name VARCHAR(50),
    proposed_start_at DATE,
    proposed_end_at DATE,
    proposed_times INTEGER,
    proposed_day_of_week VARCHAR(20),
    proposed_start_time TIME,
    proposed_end_time TIME,
    proposed_period INTEGER,
    proposed_description TEXT,

    motivation TEXT,
    status VARCHAR(20) NOT NULL DEFAULT 'PENDING',
    reviewed_by BIGINT,
    reviewed_at TIMESTAMP,
    review_note TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (applicant_id) REFERENCES users(id),
    FOREIGN KEY (subject_id) REFERENCES subjects(id),
    FOREIGN KEY (proposed_classroom_id) REFERENCES classrooms(id),
    FOREIGN KEY (reviewed_by) REFERENCES users(id)
);

CREATE INDEX idx_volunteer_applications_applicant_id ON volunteer_applications(applicant_id);
CREATE INDEX idx_volunteer_applications_status ON volunteer_applications(status);

-- 학생 등록 신청: 야학 학생이 되고 싶은 사람 (user 계정 불필요)
-- 승인 시: students 레코드 생성 후 student_id 연결
CREATE TABLE student_registration_requests (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(50) NOT NULL,
    phone_number VARCHAR(20),
    birth_date DATE,
    message TEXT,
    status VARCHAR(20) NOT NULL DEFAULT 'PENDING',
    reviewed_by BIGINT,
    reviewed_at TIMESTAMP,
    review_note TEXT,
    student_id BIGINT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (reviewed_by) REFERENCES users(id),
    FOREIGN KEY (student_id) REFERENCES students(id)
);

CREATE INDEX idx_student_registration_requests_status ON student_registration_requests(status);

-- 수강 등록 신청: 등록된 학생이 특정 과목 수강 신청
-- 승인 시: student_enrollments 레코드 생성
