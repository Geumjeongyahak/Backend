-- 수강 등록: subject_enrollment_requests 승인 후 생성
-- 한 학생이 한 과목에 중복 등록 불가
CREATE TABLE student_enrollments (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    student_id BIGINT NOT NULL,
    subject_id BIGINT NOT NULL,
    enrollment_request_id BIGINT,
    status VARCHAR(20) NOT NULL DEFAULT 'ACTIVE',
    enrolled_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    withdrawn_at TIMESTAMP,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (student_id) REFERENCES students(id),
    FOREIGN KEY (subject_id) REFERENCES subjects(id),
    FOREIGN KEY (enrollment_request_id) REFERENCES subject_enrollment_requests(id),
    CONSTRAINT uk_student_enrollment UNIQUE (student_id, subject_id)
);

CREATE INDEX idx_student_enrollments_student_id ON student_enrollments(student_id);
CREATE INDEX idx_student_enrollments_subject_id ON student_enrollments(subject_id);
