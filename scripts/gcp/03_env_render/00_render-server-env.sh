#!/usr/bin/env bash
set -euo pipefail

ENV_FILE="${1:?usage: scripts/gcp/03_env_render/00_render-server-env.sh scripts/gcp/00_env/prod.env app|db}"
TARGET="${2:?target must be app or db}"
# shellcheck disable=SC1090
source "${ENV_FILE}"

DB_INTERNAL_IP_ACTUAL="$(gcloud compute addresses describe "${DB_INTERNAL_IP_NAME}" --project="${PROJECT_ID}" --region="${REGION}" --format='value(address)' 2>/dev/null || true)"
APP_INTERNAL_IP_ACTUAL="$(gcloud compute instances describe "${APP_INSTANCE_NAME}" --project="${PROJECT_ID}" --zone="${ZONE}" --format='value(networkInterfaces[0].networkIP)' 2>/dev/null || true)"
APP_STATIC_IP="$(gcloud compute addresses describe "${APP_STATIC_IP_NAME}" --project="${PROJECT_ID}" --region="${REGION}" --format='value(address)' 2>/dev/null || true)"
API_BASE_URL="${API_DOMAIN:+https://${API_DOMAIN}}"
if [[ -z "${API_BASE_URL}" && -n "${APP_STATIC_IP}" ]]; then
  API_BASE_URL="http://${APP_STATIC_IP}:8080"
fi
APP_DB_CIDR_ACTUAL="${APP_DB_CIDR:-}"
if [[ -z "${APP_DB_CIDR_ACTUAL}" && -n "${APP_INTERNAL_IP_ACTUAL}" ]]; then
  APP_DB_CIDR_ACTUAL="${APP_INTERNAL_IP_ACTUAL}/32"
fi
APP_DB_CIDR_ACTUAL="${APP_DB_CIDR_ACTUAL:-100.64.0.0/10}"
OTEL_TRACES_SAMPLER_ARG_DEFAULT=0.05
if [[ "${ENVIRONMENT}" == "dev" ]]; then
  OTEL_TRACES_SAMPLER_ARG_DEFAULT=1.0
fi

case "${TARGET}" in
  app)
    cat <<EOF
SPRING_PROFILES_ACTIVE=${ENVIRONMENT}
APP_PORT=8080
MANAGEMENT_PORT=9090
NODE_EXPORTER_PORT=9100
API_DOMAIN=${API_DOMAIN:-}
ENABLE_CADDY=${ENABLE_CADDY:-true}
TAILSCALE_TAGS=${APP_TAILSCALE_TAGS:-}
TAILSCALE_ACCEPT_DNS=${APP_TAILSCALE_ACCEPT_DNS:-false}
